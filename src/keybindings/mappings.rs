use winit::keyboard::NamedKey;

/// Each entry is either a NamedKey or a character string
#[derive(Clone, Debug, PartialEq, Eq, PartialOrd, Ord)]
pub enum MappedKey {
    Named(NamedKey),
    Character(String),
}

pub static STR_TO_KEY: &[(&str, MappedKey)] = &[
    ("a", MappedKey::Character(String::new())),  // placeholder, will use lazy approach
];

