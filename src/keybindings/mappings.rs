pub fn mapped_key_to_str(key: &MappedKey) -> Option<&'static str> {
    match key {
        MappedKey::Character(c) => match c.as_str() {
            "a" => Some("a"), "b" => Some("b"), "c" => Some("c"), "d" => Some("d"),
            "e" => Some("e"), "f" => Some("f"), "g" => Some("g"), "h" => Some("h"),
            "i" => Some("i"), "j" => Some("j"), "k" => Some("k"), "l" => Some("l"),
            "m" => Some("m"), "n" => Some("n"), "o" => Some("o"), "p" => Some("p"),
            "q" => Some("q"), "r" => Some("r"), "s" => Some("s"), "t" => Some("t"),
            "u" => Some("u"), "v" => Some("v"), "w" => Some("w"), "x" => Some("x"),
            "y" => Some("y"), "z" => Some("z"),
            "0" => Some("0"), "1" => Some("1"), "2" => Some("2"), "3" => Some("3"),
            "4" => Some("4"), "5" => Some("5"), "6" => Some("6"), "7" => Some("7"),
            "8" => Some("8"), "9" => Some("9"),
            "`" => Some("`"), "@" => Some("@"), "*" => Some("*"), "-" => Some("-"),
            "=" => Some("="), "+" => Some("+"), "[" => Some("["), "]" => Some("]"),
            "\\" => Some("\\"), ";" => Some(";"), ":" => Some(":"), "'" => Some("'"),
            "," => Some(","), "." => Some("."), "/" => Some("/"),
            _ => None,
        },
        MappedKey::Named(named) => match named {
            NamedKey::F1 => Some("F1"), NamedKey::F2 => Some("F2"), NamedKey::F3 => Some("F3"),
            NamedKey::F4 => Some("F4"), NamedKey::F5 => Some("F5"), NamedKey::F6 => Some("F6"),
            NamedKey::F7 => Some("F7"), NamedKey::F8 => Some("F8"), NamedKey::F9 => Some("F9"),
            NamedKey::F10 => Some("F10"), NamedKey::F11 => Some("F11"), NamedKey::F12 => Some("F12"),
            NamedKey::ArrowUp => Some("Up"), NamedKey::ArrowRight => Some("Right"),
            NamedKey::ArrowDown => Some("Down"), NamedKey::ArrowLeft => Some("Left"),
            NamedKey::Escape => Some("Escape"), NamedKey::Tab => Some("Tab"),
            NamedKey::Insert => Some("Insert"), NamedKey::Delete => Some("Delete"),
            NamedKey::Backspace => Some("Backspace"), NamedKey::Enter => Some("Enter"),
            NamedKey::Home => Some("Home"), NamedKey::End => Some("End"),
            NamedKey::PageUp => Some("PageUp"), NamedKey::PageDown => Some("PageDown"),
            NamedKey::Space => Some("Space"),
            _ => None,
        },
    }
}
